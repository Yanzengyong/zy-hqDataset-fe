export const getDatasetCover = (dataset) => {
  const text = [
    dataset?.name,
    dataset?.summary,
    dataset?.domain,
    dataset?.category,
    dataset?.taskType,
    ...(dataset?.tags ?? []),
  ].filter(Boolean).join(' ')

  const matchedRule = coverRules.find((rule) =>
    rule.keywords.every((keyword) => text.includes(keyword)),
  )

  if (matchedRule) {
    return matchedRule.image
  }

  const seed = String(dataset?.id ?? dataset?.name ?? '')
    .split('')
    .reduce((total, char) => total + char.charCodeAt(0), 0)

  return coverImages[seed % coverImages.length]
}
